import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const publishedDir = path.join(rootDir, "src/content/writing/published");
const outputPath = path.join(rootDir, "netlify/functions/_shared/published-writing-slugs.ts");

function frontmatterValue(frontmatter, key) {
	const match = frontmatter.match(new RegExp(`^${key}:\\s*["']?([^"'\n]+)["']?\\s*$`, "m"));
	return match?.[1]?.trim() ?? null;
}

function fallbackSlug(filename) {
	return filename.replace(/\.md$/, "").replace(/^\d{4}-\d{2}-/, "");
}

async function publishedWritingSlugs() {
	const entries = await readdir(publishedDir, { withFileTypes: true });
	const slugs = [];

	for (const entry of entries) {
		if (!entry.isFile() || !entry.name.endsWith(".md")) continue;

		const contents = await readFile(path.join(publishedDir, entry.name), "utf8");
		const frontmatter = contents.match(/^---\n([\s\S]*?)\n---/)?.[1] ?? "";
		const draft = frontmatterValue(frontmatter, "draft");

		if (draft !== "false") continue;

		slugs.push(frontmatterValue(frontmatter, "slug") ?? fallbackSlug(entry.name));
	}

	return [...new Set(slugs)].sort();
}

const slugs = await publishedWritingSlugs();
const output = `// Generated by scripts/sync-published-writing-slugs.mjs. Do not edit by hand.
export const publishedWritingSlugs = ${JSON.stringify(slugs, null, "\t")} as const;
`;

await writeFile(outputPath, output);
console.log(`Synced ${slugs.length} published writing slug${slugs.length === 1 ? "" : "s"}.`);
